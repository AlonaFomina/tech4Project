package operator.arithcmeticOperators;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int number1 = 5,number2 = 3;

        number1 = 5;




        //addition
        int sum = number1 + number2;//8

        //subtraction
         int sub = number1- number2;//2

        //multiplication
        int times = number1 * number2;//15

        //division
        int div = number1/ number2;

        //reminder
        int rem = number1 % number2;// 5 - (3 * 1) = 2

        System.out.println("Addition = " + sum);
        System.out.println("Subtraction is =" + sub);
        System.out.println("Multiplication = " + times);
        System.out.println("Division = " + div);
        System.out.println("Reminder = " + rem);




    }
}
